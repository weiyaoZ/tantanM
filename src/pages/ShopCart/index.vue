<script>
import { mapGetters } from "vuex";
import throttle from "lodash/throttle";
export default {
  name: "ShopCart",
  mounted() {
    this.getDate();
  },
  methods: {
    getDate() {
      // 获取个人购物车数据
      this.$store.dispatch("shopCart/getCartList");
    },
    // 修改某一产品的个数（运用节流，防止出现非法数字）
    handler: throttle(async function (type, disNum, cart) {
      // type：为了区别这三个元素
      // disNum 形参：+ 变化量（1） - 变化量（-1） input 最终的个数（并不是变化的量）
      // cart：哪一个产品【身上有 id】
      switch (type) {
        case "add":
          disNum = 1;
          break;
        case "minus":
          disNum = cart.skuNum > 1 ? -1 : 0;
          break;
        case "change":
          // 用户输入进来的是最终的量，如果是非法（出现汉字|小数），带给服务器的数字为 0
          if (isNaN(disNum) || disNum < 1) {
            disNum = 0;
          } else {
            // 属于正藏的情况（小数：取整），带给服务器是变化的量= 输入进来的个数 -原来的个数
            disNum = parseInt(disNum) - cart.skuNum;
          }
        // 三元
        // disNum = (isNaN(disNum) || disNum < 0) ? 0 : parseInt(disNum) - cart.skuNum
      }
      try {
        // 代表的是修改成功
        await this.$store.dispatch("detail/addOrUpdateShopCart", {
          skuId: cart.skuId,
          skuNum: disNum,
        });
        // 再次发起请求，获取购物车的数据
        this.getDate();
      } catch (error) {
        alert(error.message);
      }
    }, 1000),
    // 删除某一个产品的操作
    async deleteCartById(cart) {
      let flag = confirm("确定要删除？");
      if (flag) {
        try {
          // 如果删除成功再次发请求获取购物车的数据进行展示
          await this.$store.dispatch(
            "shopCart/deleteCartListBySkuId",
            cart.skuId
          );
          this.getDate();
        } catch (error) {
          alert(error.message);
        }
      }
    },
    // 修改某一产品的选中状态
    async updateChecked(cart, event) {
      // 带给服务器的参数 isChecked，不是布尔值，应该是 0 或者 1
      try {
        let isChecked = event.target.checked ? "1" : "0";
        await this.$store.dispatch("shopCart/updateCheckedById", {
          skuId: cart.skuId,
          isChecked,
        });
        // 再次请求数据
        this.getDate();
      } catch (error) {
        alert(error.message);
      }
    },
    // 删除全部选中的商品
    async deleteAllCheckedCart() {
      try {
        // 派发一个 action
        await this.$store.dispatch("shopCart/deleteAllCheckedCart");
        // 再次获取数据
        this.getDate();
      } catch (error) {
        alert(error.message);
      }
    },
    // 修改全部产品的选中状态
    async updateAllCartChecked(event) {
      if (this.cartInfoList.length > 0) {
        try {
          let isChecked = event.target.checked ? "1" : "0";
          // 派发 action
          await this.$store.dispatch("shopCart/updateAllCartChecked",isChecked);
          // 再次获取数据
          this.getDate();
        } catch (error) {
          alert(error.message);
        }
      }
    },
  },
  computed: {
    ...mapGetters("shopCart", ["carList"]),
    // 购物车的数据
    cartInfoList() {
      // 起码是个数组
      return this.carList.cartInfoList || [];
    },
    // 计算购买产品的总价
    totalPrice() {
      let sum = 0;
      this.cartInfoList.forEach((item) => {
        if (item.isChecked == 1) sum += item.skuPrice * item.skuNum;
      });
      return sum;
    },
    // 计算商品总数
    totalNum() {
      let num = 0;
      this.cartInfoList.forEach((item) => {
        if (item.isChecked == 1) num += item.skuNum;
      });
      return num;
    },
    // 判断底部复选框是否勾选【全部产品都选中才勾选】
    isAllChecked() {
      // 数组的 every() 方法
      // 判断数组中的元素是否相同，相同的话返回 true，否则为 false
      return this.cartInfoList.every((item) => item.isChecked);
    },
  },
};
</script>

